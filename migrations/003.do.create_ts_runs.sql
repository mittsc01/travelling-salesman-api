CREATE TABLE ts_runs (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  date TEXT NOT NULL,
  title TEXT NOT NULL,
  created_by INTEGER
    REFERENCES ts_users(id) ON DELETE SET NULL NOT NULL,
  route_id INTEGER
    REFERENCES ts_routes(id) ON DELETE SET NULL NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL,
  date_modified TIMESTAMP
);